a = 100
print(a, type(a))  # int datatype
b = 20.8
print(b, type(b))  # float datatype
c = 20 + 3j
print(c, type(c))  # complex datatype
d = [10, 200]
d[1] = 20  # changeable datatype
print(d, type(d))  # list datatype
e = (10, "Hello World", 235)
print(e, type(e))  # tupal datatype
f = {
    'courseName': "Python"
}
print(f, type(f))  # dictionary datatype
g = {10, 20, "Hi", 10}
print(g, type(g))  # set datatype
