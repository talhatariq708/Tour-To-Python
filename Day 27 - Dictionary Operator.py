# dictionary operator

a = {
    'name': 'Talha Tariq',
    'class': 'intermediate',
    'programming language': 'python',
    'session': 27
}
print(a)
for n in a:
    print("")  # this will make one line gap
    print(n)  # this will print only keys
    print(a[n])  # this will print key's statements
    print("")  # this will make on line gap
